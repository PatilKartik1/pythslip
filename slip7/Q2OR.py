''' Write a Python program to unpack a tuple in several variables. Display type of each 
variable'''


# Define a tuple
tuple_var = (10, 20, 30)

# Unpack the tuple into several variables
var1, var2, var3 = tuple_var

# Display type of each variable
print("Type of var1:", type(var1))
print("Type of var2:", type(var2))
print("Type of var3:", type(var3))
